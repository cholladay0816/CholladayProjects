<h3>About</h3>
<p>
  This is a blackjack program I made for my senior year project in high school.  This game is played by clicking the flipped card to hit, and the stand button to stand.
  Your opponent is the dealer, who stops on 17.  When both players stand -- or one busts out -- the game displays the winner before restarting.<br />
  The game also keeps a running log of all the actions that take place. <br />
  The dealer also has his own table, which can be seen at all times for the player.  I added this to create a tabletop aesthetic.
</p>
